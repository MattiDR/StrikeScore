package com.example.strikescore.ui.util

/**
 * Enumeration class representing different types of navigation in the Strike Score application.
 *
 * This enum class defines the following navigation types:
 * - [BOTTOM_NAVIGATION]: Navigation using a bottom navigation bar.
 * - [NAVIGATION_RAIL]: Navigation using a navigation rail.
 * - [PERMANENT_NAVIGATION_DRAWER]: Navigation using a permanent navigation drawer.
 */
enum class StrikeScoreNavigationType {
    BOTTOM_NAVIGATION, NAVIGATION_RAIL, PERMANENT_NAVIGATION_DRAWER
}